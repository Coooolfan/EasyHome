}

// Ëß£Êûê‰ª∑Ê†ºËåÉÂõ¥
const parsePriceRange = (priceRange: string) => {
  if (!priceRange) return { minPrice: null, maxPrice: null }
  
  const [min, max] = priceRange.split('-').map(p => parseInt(p))
  
  if (max >= 999999) {
    return { minPrice: min, maxPrice: null }
  }
  
  return { minPrice: min, maxPrice: max }
}

